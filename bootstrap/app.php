<?php

use Illuminate\Foundation\Application;
use Illuminate\Foundation\Configuration\Exceptions as ConfigExceptions;
use Illuminate\Foundation\Configuration\Middleware as ConfigMiddleware;
use Spatie\Permission\Middleware\PermissionMiddleware;
use Spatie\Permission\Middleware\RoleMiddleware;
use Spatie\Permission\Middleware\RoleOrPermissionMiddleware;
use App\Http\Middleware\RemoveBOM;

return Application::configure(basePath: dirname(__DIR__))
    ->withRouting(
        web: __DIR__.'/../routes/web.php',
        api: __DIR__.'/../routes/api.php',
        commands: __DIR__.'/../routes/console.php',
        health: '/up',
    )
    ->withMiddleware(function (ConfigMiddleware $middleware) {
        $middleware->append(RemoveBOM::class);
        $middleware->alias([
            'role' => RoleMiddleware::class,
            'permission' => PermissionMiddleware::class,
            'role_or_permission' => RoleOrPermissionMiddleware::class,
        ]);
    })
    ->withExceptions(function (ConfigExceptions $exceptions) {
        //
    })->create();
